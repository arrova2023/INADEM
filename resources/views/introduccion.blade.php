<h1>Introducción</h1>
<p align="justify">

    El presente registro, tiene como objetivo generar el Banco Nacional de Tecnologías, Proyectos o  Prototipos del Tecnológico Nacional de México (TecNM). Este Banco Nacional, servirá de base para la participación en convocatorias que el TecNM solo o en conjunto con otras instituciones, emita para la participación en fondos, que estimulen el desarrollo tecnológico y la  innovación de alto impacto. Así también busca articular los programas requeridos para desarrollar, validar,  y consolidar las propuestas presentadas e identificar Tecnologías, proyectos o prototipos que se encuentren en posibilidades de ser comercializados o licenciados.


</p>
